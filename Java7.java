public class Java7 {
    public static void main(String[] args) {
       int x = 5;
       int y = 5;

       int a1 = x+y;
       System.out.println("x + y: "+ a1);
       
       int a2 = x-y;
       System.out.println("x - y: "+ a2);

       int a3 = x*y;
       System.out.println("x * y: "+ a3);

       int a4 = x/y;
       System.out.println("x / y: "+ a4);

       int a5 = x%y;
       System.out.println("x % y: "+ a5);
    }
    
}
